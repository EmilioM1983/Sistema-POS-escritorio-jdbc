    public void calcularTotales(){
    
        DefaultTableModel modelo = (DefaultTableModel) tbResumenVentas.getModel();
        
        double total = 0;
        
        DecimalFormat formato = new DecimalFormat("#.##");
        
        for (int i = 0; i < modelo.getRowCount(); i++) {
            total = Double.parseDouble(formato.format(total + (double) modelo.getValueAt(i, 4)));
            
        }
        lblTotal.setText(String.valueOf(total));
    }
    
    
