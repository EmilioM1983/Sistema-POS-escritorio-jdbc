    private void btnAgregarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarProductoActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel modelo = (DefaultTableModel) tbResumenVentas.getModel();
        
        int stockDisponible = Integer.parseInt(txtSStockProducto.getText());
        
        String idProducto = txtIdProducto.getText();
        
        for (int i = 0; i < modelo.getRowCount(); i++) {
            String idExiste = (String) modelo.getValueAt(i, 0);
            if (idExiste.equals(idProducto)) {
                JOptionPane.showMessageDialog(null, "El producto ya esta registrado");
                return;
            }
        }
        
        String nombreProducto = txtSNombreProducto.getText();
        double precioUnitario = Double.parseDouble(txtSPrecioVenta.getText());
        int cantidadVendida = Integer.parseInt(txtCantidadVentas.getText());
        
        if (cantidadVendida > Integer.parseInt(txtSStockProducto.getText())) {
            JOptionPane.showMessageDialog(null, "La cantidad de ventas no puede ser mayor al stock disponible");
            return;
        }
        
        double subtotal = precioUnitario * cantidadVendida;
        
        modelo.addRow(new Object[]{idProducto, nombreProducto, precioUnitario, cantidadVendida, subtotal});
        
        for (int column = 0; column < tbResumenVentas.getColumnCount(); column++) {
            Class<?> columnClass = tbResumenVentas.getColumnClass(column);
            tbResumenVentas.setDefaultEditor(columnClass, null);
        }
        
        calcularTotales();
        deshabilitar();
    }//GEN-LAST:event_btnAgregarProductoActionPerformed

    private void btnEliminarResumenVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarResumenVentaActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel)tbResumenVentas.getModel();
        int indiceSeleccionado = tbResumenVentas.getSelectedRow();
        
        if (indiceSeleccionado != -1) {
            modelo.removeRow(indiceSeleccionado);
            calcularTotales();
        }
        else{
            JOptionPane.showMessageDialog(null, "Error al seleccionar");
        }
    }//GEN-LAST:event_btnEliminarResumenVentaActionPerformed

