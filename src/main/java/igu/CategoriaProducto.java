    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        if (tbCategorias.getSelectedRow()!=-1) {
            categoria.modificarCategoria(txtId, txtNombreCategoria);
            categoria.mostrarCategorias(tbCategorias);         
        }
        else{
            JOptionPane.showMessageDialog(null, "Por favor, seleccione una categoria de la tabla.");
        }
        deshabilitar();
        limpiarCampos();
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (tbCategorias.getSelectedRow()!=-1) {
            categoria.eliminarCategoria(txtId);
            categoria.mostrarCategorias(tbCategorias);         
        }
        else{
            JOptionPane.showMessageDialog(null, "Por favor, seleccione una categoria de la tabla.");
        }
        deshabilitar();
        limpiarCampos();
    }//GEN-LAST:event_btnEliminarActionPerformed

    public void limpiarCampos(){
        txtNombreCategoria.setText("");
        
    }
    
    public void habilitar() {
        txtNombreCategoria.enable(true);
        
    }

    public void deshabilitar() {
        txtNombreCategoria.enable(false);
        
    }

