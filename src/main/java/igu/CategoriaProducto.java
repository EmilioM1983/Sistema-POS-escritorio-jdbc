    private void tbCategoriasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbCategoriasMouseClicked
        // TODO add your handling code here:

        txtId.setText(tbCategorias.getValueAt(tbCategorias.getSelectedRow(), 0).toString());
        txtNombreCategoria.setText(tbCategorias.getValueAt(tbCategorias.getSelectedRow(), 1).toString());
        
        deshabilitar();
    }//GEN-LAST:event_tbCategoriasMouseClicked

